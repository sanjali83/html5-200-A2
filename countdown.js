let i=0;
console.log ("For loop Output");
for (i=10; i>0 ; i--) {
  console.log(i);
}

i=20; // reassign value of i

console.log ("While loop Output")
while (i>=11) {
  console.log(i);
  i--;
}

i=30; // reassign value of i
console.log ("DO While loop Output")
do{
console.log(i);
  i--;
} while (i>20);
